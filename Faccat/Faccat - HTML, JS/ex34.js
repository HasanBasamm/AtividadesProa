let x, y, z, resposta;
x = parseFloat(prompt("Informe o valor de X "));
y = parseFloat(prompt("Informe o valor de Y "));
z = (x * y) + 5;

alert("A resposta de "+x+" +"+y+" é: "+z);

if (z < 0) {
    alert (resposta = "A resposta é A");
} else if (z < 100) {
    alert (resposta = "A resposta é B");
} else {
    alert (resposta = "A resposta é C");
}
